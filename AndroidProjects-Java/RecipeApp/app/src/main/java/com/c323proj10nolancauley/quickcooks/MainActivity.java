public class MainActivity extends AppCompatActivity implements NavigationView.OnNavigationItemSelectedListener{
    private Toolbar toolbar;
    private DrawerLayout drawerLayout;
    private FrameLayout frameLayout;
    private NavigationView navigationView;
    private AdView adView;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);

        //initialize ads and find view
        MobileAds.initialize(this, new OnInitializationCompleteListener() {
            @Override
            public void onInitializationComplete(InitializationStatus initializationStatus) {
            }
        });
        adView = findViewById(R.id.mainBannerAd);
        AdRequest adRequest = new AdRequest.Builder().build();
        adView.loadAd(adRequest);

        initializeViews();
        toggleDrawer();
    }

    public void initializeViews(){
    //toolbar
    toolbar = findViewById(R.id.toolbar_id);
    toolbar.setTitle(R.string.toolbar_title);
    setSupportActionBar(toolbar);
    //nav drawer
    drawerLayout = findViewById(R.id.drawer_layout_id);
    frameLayout = findViewById(R.id.framelayout_id);
    navigationView = findViewById(R.id.navigationview_id);
    navigationView.setNavigationItemSelectedListener(this);

    }

    //nav drawer functionality
    private void toggleDrawer() {
        ActionBarDrawerToggle drawerToggle = new ActionBarDrawerToggle(this, drawerLayout, toolbar,
                R.string.navigation_drawer_open, R.string.navigation_drawer_close);
        drawerLayout.addDrawerListener(drawerToggle);
        drawerToggle.syncState();
    }
    @Override
    public void onBackPressed() {
        //Checks if the navigation drawer is open -- If so, close it
        if (drawerLayout.isDrawerOpen(GravityCompat.START)) {
            drawerLayout.closeDrawer(GravityCompat.START);
        }
        // If drawer is already close -- Do not override original functionality
        else {
            super.onBackPressed();
        }
    }
    @Override
    public boolean onNavigationItemSelected(@NonNull MenuItem item) {
        switch (item.getItemId()) {
            case R.id.favoriteRecipes:
                FavoriteRecipesFragment dialog = new FavoriteRecipesFragment();
                dialog.show(getSupportFragmentManager(), "Favorites");
                closeDrawer();
                break;
            default:
            case R.id.categories:
                getSupportFragmentManager().beginTransaction().replace(R.id.framelayout_id, new CategoryFragment())
                        .commit();
                closeDrawer();
                break;
        }
        return true;
    }
    //Checks if the navigation drawer is open - if so, close it
    private void closeDrawer(){
        if (drawerLayout.isDrawerOpen(GravityCompat.START)){
            drawerLayout.closeDrawer(GravityCompat.START);
        }
    }
}